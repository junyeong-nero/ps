    # 마름모를 어떻게 집어넣느냐에 따라 달라지는 것 같은데. 
    # 어짜피 나머지는 삼각형으로 다 채워버릴 수 있음.
    # n = 10^5 => n log n 으로 풀어야 함
    
    # recursive 하게 볼 수 잇을 듯
    # solution(n - 1, tops[1:]) 을 호출하는 방식으로?
    # 그래도 문제가 잇는게 이전 것과 겹치게 마름모를 놓는 경우도 있을 수 있다.
    
    # 그래프 느낌으로 볼 수도. 결국 인접한 삼각형 중 어느것과 합칠지 / 말지 결정하는 것.
    # 뒤집어진 삼각형 기준으로 보면 편할 것 같다.
    # i 번째 뒤집어진 삼각형이 왼쪽과 합쳐지는 경우, 오른쪽 혹은 위.
    # k = 0 : 결합 안함 / 1: 왼쪽 / 2 : 오른쪽 / 3 : 위
    
    # dp[i][k] = i 번째 삼각형이 k 와 결합했을 때 경우의 수
    # k 번째 삼각형의 위에 뿔이 있는지/없는지 나눠서 생각해야 되긴함.
    
    dp = [[0, 0, 0, 0] for _ in range(n + 1)]
    dp[0] = [1, 0, 0, 0]
    
    # 뿔이 없는 경우
    for i in range(1, n + 1):
        
        temp = sum(dp[i - 1]) 
        dp[i][0] = temp
        dp[i][1] = temp - dp[i - 1][1] # 왼쪽 결합하면 안됨
        dp[i][2] = temp 
        
        if tops[i - 1] == 0:
            dp[i][3] = 0
        else:
            dp[i][3] = temp
        
    return sum(dp[-1])



