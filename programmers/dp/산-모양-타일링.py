def solution(n, tops):
    MOD = 10007

    # a: i번째 산까지 타일을 채웠을 때,
    #    "오른쪽으로 걸쳐진 미완성 없이" 깔끔하게 끝나는 경우의 수
    #
    # b: i번째 산까지 타일을 채웠을 때,
    #    "다음 산과 이어질 수 있는 한쪽 경계가 열린 상태"로 끝나는 경우의 수
    #
    # 이 문제는 현재 칸 하나의 상태보다,
    # "i번째까지 채운 뒤 경계가 어떤 모양이냐"가 중요하다.

    # i = 1일 때 초기값
    #
    # a = 1:
    # 첫 번째 산까지 채우고 깔끔하게 끝나는 기본 경우 1개
    #
    # b:
    # 첫 번째 산을 채운 뒤 경계가 열린 상태로 끝나는 경우의 수
    # - tops[0] == 0 이면 2가지
    # - tops[0] == 1 이면 위 삼각형이 하나 더 있으므로 3가지
    a = 1
    b = 3 if tops[0] else 2

    # 이제 2번째 산부터 n번째 산까지 차례대로 확장
    for i in range(1, n):
        # 새로 계산할 상태
        #
        # na = a[i]
        # nb = b[i]

        # 현재 산까지 "깔끔하게 끝나는 경우"는
        # 이전 산까지의 모든 경우(a, b)에서 만들 수 있다.
        #
        # 왜냐하면 이전이
        # - 이미 닫힌 상태(a)였든
        # - 한쪽이 열린 상태(b)였든
        # 현재 산을 채우면서 깔끔하게 마무리하는 배치가 존재하기 때문
        na = (a + b) % MOD

        if tops[i]:
            # 현재 산 꼭대기에 삼각형이 하나 더 있는 경우
            #
            # 이전이 a(닫힘)였을 때:
            #   현재를 "열린 상태"로 끝내는 방법이 2가지
            #
            # 이전이 b(열림)였을 때:
            #   현재를 "열린 상태"로 끝내는 방법이 3가지
            #
            # 따라서
            # nb = 2*a + 3*b
            nb = (2 * a + 3 * b) % MOD
        else:
            # 현재 산 꼭대기에 추가 삼각형이 없는 경우
            #
            # 이전이 a(닫힘)였을 때:
            #   현재를 열린 상태로 끝내는 방법이 1가지
            #
            # 이전이 b(열림)였을 때:
            #   현재를 열린 상태로 끝내는 방법이 2가지
            #
            # 따라서
            # nb = a + 2*b
            nb = (a + 2 * b) % MOD

        # 다음 반복을 위해 상태 갱신
        a, b = na, nb

    # 마지막 산까지 채운 뒤의 전체 경우의 수는
    # - 깔끔하게 끝난 경우(a)
    # - 열린 상태로 끝난 경우(b)
    # 를 모두 포함한다.
    return (a + b) % MOD
